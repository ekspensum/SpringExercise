package pl.bookjava.aop;

public class Book {
        private int id;
        private String bookTitle;

        public int getId() {
                return id;
        }
        public void setId(int id) {
                this.id = id;
        }
        public String getBookTitle() {
                return bookTitle;
        }
        public void setBookTitle(String bookTitle) {
                this.bookTitle = bookTitle;
        }
}

